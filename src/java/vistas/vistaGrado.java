    private void btncreargradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreargradoActionPerformed
        if(txtgrado.getText().toString().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del grado");
        }else{
        String gradoNuevo = txtgrado.getText().toString();
        ModeloGrado modgrado = new ModeloGrado();
        modgrado.setDescripcionGrado(gradoNuevo);
        cliente.crearGrado(modgrado);
        JOptionPane.showMessageDialog(this, "Se ha añadido exitosamente");
        listaAlumnoss();
        limpiar();
        }
    }//GEN-LAST:event_btncreargradoActionPerformed

    private void btneditargradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditargradoActionPerformed
        if(txtgrado.getText().toString().equals("")||txtidgrado.getText().toString().equals("")){
            JOptionPane.showMessageDialog(this, "Seleccione el grado a editar (id y nombre)");
        }else{
        String gradoEditado = txtgrado.getText().toString();
        int idgrados = Integer.parseInt(txtidgrado.getText().toString());
        ModeloGrado modgrado = new ModeloGrado();
        modgrado.setDescripcionGrado(gradoEditado);
        modgrado.setIdGrado(idgrados);
        cliente.actualizarGrado(modgrado);
        JOptionPane.showMessageDialog(this, "Se ha actualizado exitosamente");
        listaAlumnoss();
        limpiar();
        }
    }//GEN-LAST:event_btneditargradoActionPerformed

    private void btneliminargradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminargradoActionPerformed
       if(txtidgrado.getText().toString().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese el ID del grado que desea eliminar");
        }else{
        int idgrados = Integer.parseInt(txtidgrado.getText().toString());
       cliente.eliminarGrado(idgrados);
       JOptionPane.showMessageDialog(this, "Se ha eliminado exitosamente");
        listaAlumnoss();
        limpiar();
       }
    }//GEN-LAST:event_btneliminargradoActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        if(txtidgrado.getText().toString().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un ID para la busqueda de grado");
        }else{
        int id = Integer.parseInt(txtidgrado.getText().toString());

         if(cliente.buscarGrado(id).isEmpty()){
           JOptionPane.showMessageDialog(this, "error, no existe grado");
          
         }else{
            List<ModeloGrado> listaid = cliente.buscarGrado(id);
            
            for(int i=0;i<listaid.size();i++){
                String nombregrado = listaid.get(i).getDescripcionGrado();
                System.out.println("esto es nombre grado for"+nombregrado);
                 txtgrado.setText(nombregrado);
                 
                 
            }
       
         }
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

