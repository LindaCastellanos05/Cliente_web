    public void listaAlumnoss(){
        String Titulo[]={"id_grado","nombre_grado"};
        mimodelGrado = new DefaultTableModel(null, Titulo);
        
        dataGrado = (ArrayList<ModeloGrado>) cliente.todosgrados();
        int tamano = cliente.todosgrados().size();
        
        Object[]obj = new Object[3];
        for (int i=0;i<tamano;i++){
            obj[0]=dataGrado.get(i).getIdGrado();
            obj[1]=dataGrado.get(i).getDescripcionGrado();
            
            mimodelGrado.addRow(obj);
        }
        
        tblgrado.setModel(mimodelGrado);
    }
